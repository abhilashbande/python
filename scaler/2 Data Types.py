# Integers
var = 10;
print(var)

#type
print(type(var))

# Floats
var = 3.15;
print(type(var))

# Booleans
var = True;
print(type(var))

# Strings
var = "Hello, World!";
print(type(var))

var = """
This is multiline string.
It can span multiple lines.
""";

print(var)

# None
var = None;
print(type(var))